# calculating total expances through-out the year


exp = [250,980,673,123,789,556,800,230,456,890,900,1600]
total = 0
for i in range(len(exp)):
    print("month ",i+1, ": expances ",exp[i])
    total=total+exp[i]
print ("total expances through-out year ",total)

